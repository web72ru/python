ключи = [1, 2, 3, 4, 5, 6, 7, 8]
значения = []

словарь = {1: 1, 7: 7, 10: 10, 4: 4, 2: 2}

for ключ_словаря in словарь.keys():
    if ключ_словаря  not in ключи:
        ключи.append(ключ_словаря)

for ключ in ключи:
    if ключ not in словарь:
        значения.append('')
    else:
        значения.append(словарь[ключ])

print(ключи)
print(значения)